"""
    random(x;args)

Randomly generate a vector of values at collocation points `x`, based on provided (optional arguments) :
- `L` is the typical wavelength (default is `L=1`),
- `s` is the (real) Sobolev index regularity (default is `s=‚àû`, smooth data),
- `Œª` is the length of spatial localization (default is `Œª=‚àû`, no localization),
- `a` is the amplitude of the returned vector (default is `a=1`).

The vector is generated through randomly chosen Fourier coefficients,
multiplied with weigth `w=10^(-|k|L/(2œÄ))` if `s=‚àû`, or `w=1/(1+9(|k|L/(2œÄ))^(s+1/2))` otherwise.
If `Œª‚âÝ‚àû`, the function in spatial variables is multiplied by `exp(-|x/Œª|^2)`,
and in any case normalized to have maximum absolute value 1.


"""
function random(x; L = 1, s = Inf, Œª = Inf, a = 1)


    k = Mesh(x).k
    if s == Inf
        w = 10 .^ (-abs.(k * L / (2 * œÄ)))
    else
        w = 1 ./ (1 .+ 9 * abs.(k * L / (2 * œÄ)) .^ (s + 1 / 2))
    end
    if Œª == Inf
        œÜ = zero(x) .+ 1
    else
        œÜ = exp.(-abs.((x / Œª) .^ 2))
    end

    Œ∏ = 2 * œÄ * rand(Float64, length(x))
    r = rand(Float64, length(x))
    Fourier = r .* exp.(-1im .* Œ∏) .* w
    Physic = real.(ifft(Fourier)) .* œÜ
    return Physic ./ (maximum(Physic) - minimum(Physic)) * a
end

"""
